    [RE_USE]                   CHAR (1)     NULL,
    [TRANS_OPER_UNL_MRN]       VARCHAR (21) NULL,
    [EN_ROUT_EVENT_MRN]        VARCHAR (21) NULL,
    [EN_ROUT_EVENT_SEQ]        DECIMAL (4)  NULL,
