    CONSTRAINT [SD_REC_IN_ERE] FOREIGN KEY ([EN_ROUT_EVENT_MRN], [EN_ROUT_EVENT_SEQ]) REFERENCES [ncts].[EN_ROUT_EVENT] ([MRN], [SEQ]),
    CONSTRAINT [SD_SEAL_IDENT_IN_L] FOREIGN KEY ([SEAL_IDENT_LNG]) REFERENCES [ncts].[LANG] ([ID]),
    CONSTRAINT [SD_USED_AT_SPACL] FOREIGN KEY ([TRAD_SP_AUTH_SID], [CUST_OFF_ADDR_SID]) REFERENCES [ncts].[SP_AUTH_CONTR_LOC] ([TRAD_SP_AUTH_ID], [CUST_OFF_ADDR_SID]),
    CONSTRAINT [SD_USED_FROM_GC_S] FOREIGN KEY ([FROM_HS_GOODS_CLASS_ID], [FROM_SENS_GOODS_CLASS_ID]) REFERENCES [ncts].[SENS_GOODS_CLASS] ([HS_GOODS_CLASS_ID], [SGI_COD]),
    CONSTRAINT [SD_USED_OF_VERS] FOREIGN KEY ([TRANS_OPER_SID], [VERS_NUMB]) REFERENCES [ncts].[TRANS_OPER_VERS] ([TRANS_OPER_SID], [VERS_NUMB])
